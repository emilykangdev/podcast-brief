import { readFileSync, writeFileSync, mkdirSync } from "fs";
import path from "path";
import { extractSection } from "./markdown.mjs";

// ── exported run function ─────────────────────────────────────────────────────
export async function run({ briefPath, referencesPath, outputDir } = {}) {
  outputDir = outputDir ?? path.join(process.cwd(), "briefs");

  let brief, enrichedRefs;
  try {
    brief = readFileSync(briefPath, "utf-8");
  } catch (e) {
    throw new Error(`Cannot read brief file: ${e.message}`);
  }
  try {
    enrichedRefs = readFileSync(referencesPath, "utf-8");
  } catch (e) {
    throw new Error(`Cannot read references file: ${e.message}`);
  }

  // Find and replace the # REFERENCES section
  const section = extractSection(brief, "REFERENCES");
  if (!section) {
    throw new Error("No REFERENCES section found in brief. Was this generated by extract_wisdom?");
  }

  // trimEnd/trimStart prevents stacked blank lines when sections already have trailing/leading whitespace
  const before = brief.slice(0, section.headingStart).trimEnd();
  const after = brief.slice(section.contentEnd).trimStart();
  // Strip the heading line from enrichedRefs (validate-references.mjs writes "# Enriched References\n\n...")
  const enrichedContent = enrichedRefs.replace(/^#{1,3}\s+[^\n]*\n+/, "");
  const finalBrief =
    [before, section.headingLine + "\n\n" + enrichedContent.trimEnd(), after]
      .filter(Boolean)
      .join("\n\n") + "\n";

  const finalBriefPath = writeFinal(briefPath, finalBrief, outputDir);

  return { finalBriefPath, finalBriefMd: finalBrief };
}

function writeFinal(inputPath, content, outputDir) {
  const stem = path.basename(inputPath, path.extname(inputPath));
  const podcastID = stem.replace(/-output.*$/, "");
  mkdirSync(outputDir, { recursive: true });
  const outPath = path.join(outputDir, `${podcastID}-final-brief.md`);
  writeFileSync(outPath, content, "utf-8");
  console.error(`✓ Written to ${outPath}`);
  return outPath;
}

// ── CLI shim ──────────────────────────────────────────────────────────────────
import { fileURLToPath } from "url";
if (process.argv[1] === fileURLToPath(import.meta.url)) {
  const [briefPath, referencesPath] = process.argv.slice(2);
  if (!briefPath || !referencesPath) {
    console.error(
      "Usage: node scripts/merge-references.mjs <extract-wisdom-output.md> <podcastID-references.md>"
    );
    process.exit(1);
  }
  run({ briefPath, referencesPath }).catch((err) => {
    console.error(err.message);
    process.exit(1);
  });
}
